public class Sumar {
    
    double resultado;
    public double CSuma(double v1, double v2)
    {
         resultado=v1+v2;
         return resultado;
    }
     public double CResta(double v1, double v2)
    {
         resultado=v1-v2;
         return resultado;
    }
     
    
    
}